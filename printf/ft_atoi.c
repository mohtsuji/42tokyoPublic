#include "ft_printf.h"

int	ft_atoi(char **str)
{
	int	nb;
	int	mai;

	nb = 0;
	mai = 1;
	while (**str == ' ' || **str == '\f' || **str == '\n' ||
			**str == '\r' || **str == '\t' || **str == '\v')
		(*str)++;
	if (**str == '-' || **str == '+')
	{
		if (**str == '-')
			mai = mai * -1;
		(*str)++;
	}
	while ('0' <= **str && **str <= '9')
	{
		nb = (nb * 10) + (**str - 48);
		(*str)++;
	}
	nb = nb * mai;
	return (nb);
}
